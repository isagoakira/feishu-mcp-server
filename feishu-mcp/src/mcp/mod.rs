    // ========================================================================

    /// List tasks by goal ID
    #[tool(
        description = "List Feishu tasks associated with a specific goal",
        annotations(title = "List Tasks", read_only_hint = true, idempotent_hint = true)
    )]
    async fn list_tasks(
        &self,
        params: Parameters<ListTasksInput>,
    ) -> Result<CallToolResult, McpError> {
        let input = params.0;
        rate_limit(&self.rate_limiter, "list_tasks")?;

        match self.feishu_client.list_tasks(Some(&input.goal_id)).await {
            Ok(tasks) => Ok(json_result(&tasks)),
            Err(e) => Ok(error_result(e)),
        }
    }

    /// Get task by ID
    #[tool(
        description = "Get a Feishu task by its task ID",
        annotations(title = "Get Task", read_only_hint = true, idempotent_hint = true)
    )]
    async fn get_task(
        &self,
        params: Parameters<GetTaskInput>,
    ) -> Result<CallToolResult, McpError> {
        let input = params.0;
        rate_limit(&self.rate_limiter, "get_task")?;

        match self.feishu_client.get_task(&input.task_id).await {
            Ok(task) => Ok(json_result(&task)),
            Err(e) => Ok(error_result(e)),
        }
    }

    /// Create a new task
    #[tool(
        description = "Create a new Feishu task under a specific goal",
        annotations(title = "Create Task")
    )]
    async fn create_task(
        &self,
        params: Parameters<CreateTaskInput>,
    ) -> Result<CallToolResult, McpError> {
        let input = params.0;
        rate_limit(&self.rate_limiter, "create_task")?;

        match self
            .feishu_client
            .create_task(
                &input.title,
                None,
                input.assignee_id.as_deref(),
                input.due_date.as_deref(),
                Some(&input.goal_id),
            )
            .await
        {
            Ok(task) => Ok(json_result(&task)),
            Err(e) => Ok(error_result(e)),
        }
    }

    /// Update task status
    #[tool(
        description = "Update the status of a Feishu task (todo, in_progress, or done)",
        annotations(title = "Update Task Status", idempotent_hint = true)
    )]
    async fn update_task_status(
        &self,
        params: Parameters<UpdateTaskStatusInput>,
    ) -> Result<CallToolResult, McpError> {
        let input = params.0;
        rate_limit(&self.rate_limiter, "update_task_status")?;

        use crate::feishu::types::TaskStatus;
        let task_status = match input.status.as_str() {
            "todo" => TaskStatus::Todo,
            "in_progress" => TaskStatus::InProgress,
            "done" => TaskStatus::Done,
            _ => {
                return Ok(error_result_msg(format!(
                    "Invalid status: '{}'. Expected 'todo', 'in_progress', or 'done'",
                    input.status
                )));
            }
        };

        match self
            .feishu_client
            .update_task_status(&input.task_id, task_status)
            .await
        {
            Ok(task) => Ok(json_result(&task)),
            Err(e) => Ok(error_result(e)),
        }
    }

