/// Check rate limit for a tool call
fn rate_limit(limiter: &RateLimiter, tool: &str) -> Result<(), McpError> {
    if let Err(e) = limiter.check(tool) {
        return Err(McpError::internal_error(
            format!("Rate limit exceeded: retry after {}s", e.retry_after_secs),
            None,
        ));
    }
    Ok(())
}

/// Convert a serializable object to a JSON CallToolResult
fn json_result<T: serde::Serialize>(value: &T) -> CallToolResult {
    let json = serde_json::to_value(value).unwrap_or_default();
    CallToolResult::success(vec![Content::text(json.to_string())])
}

/// Convert a FeishuError to an error CallToolResult
fn error_result(err: FeishuError) -> CallToolResult {
    CallToolResult::error(vec![Content::text(err.to_string())])
}

/// Create an error CallToolResult from a string message
fn error_result_msg(msg: String) -> CallToolResult {
    CallToolResult::error(vec![Content::text(msg)])
}
